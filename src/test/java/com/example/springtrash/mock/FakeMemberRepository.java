package com.example.springtrash.mock;

import com.example.springtrash.member.domain.Member;
import com.example.springtrash.member.service.port.MemberRepository;
import java.util.HashMap;
import java.util.Map;
import java.util.Optional;

public class FakeMemberRepository implements MemberRepository {

    private final Map<Long, Member> database = new HashMap<>();

    private long autoGeneratedId = 0;

    @Override
    public boolean isDuplicateId(String loginId) {
        return database.values().stream()
                .anyMatch(member ->
                        member.getLoginId().equalsIgnoreCase(loginId));
    }

    @Override
    public void join(Member member) {
        database.put(++autoGeneratedId, member);
    }

    @Override
    public Optional<Member> findById(Long id) {
        return Optional.ofNullable(database.get(id));
    }

    @Override
    public Optional<Member> findByLoginId(String loginId) {
        return database.values()
                .stream()
                .filter(member ->
                        member.getLoginId().equals(loginId))
                .findFirst();
    }

    @Override
    public void login(Member member) {
        database.put(member.getId(),member);
    }

    @Override
    public void updateMember(Member member) {
        database.put(member.getId(), member);
    }
}
